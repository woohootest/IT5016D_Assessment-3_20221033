quote = "God Defend New Zealand."
print(quote)
print("\nIn uppercase:")
print(quote.upper())
print("\nIn lowercase:")
print(quote.lower())
print("\nAs a title:")
print(quote.title())
print("\nWith a minor replacement:")
print(quote.replace("New Zealand", "Aotearoa"))
print("\nOriginal quote is still:")
print(quote)
print("\nIn swapcase:")
print(quote.swapcase())


